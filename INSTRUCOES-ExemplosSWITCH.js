var diaSemana = "Quinta-feira";

switch(diaSemana){
    case "Segunda-feira":
        console.log("Hoje é Segunda-feira, faltam ainda 4 dias para o final de semana");
        break;
    case "Terça-feira":
        console.log("Hoje é Terça-feira, faltam ainda 3 dias para o final de semana");
        break;
    case "Quarta-feira":
        console.log("Hoje é Quarta-feira, faltam ainda 2 dias para o final de semana");
        break;
    case "Quinta-feira":
        console.log("Hoje é Quinta-feira, faltam ainda 1 dia para o final de semana");
        break;
    case "Sexta-feira":
        console.log("Hoje é Sexta-feira, o final de semana já começou, SEXXXTOOOUUUU!!");
        break;
    default:
        console.log("Hoje não é um dia da semana válido");
        break;
}
console.log("Estamos falando da semana! Digita aí o dia da semana que você quer saber!");